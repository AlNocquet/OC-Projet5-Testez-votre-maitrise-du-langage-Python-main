
def sum(a, b):
    """
    Calcule la somme de deux nombres.


    Paramètres:
    a (int or float): Le premier nombre.
    b (int or float): Le deuxième nombre.


    Retourne:
    int or float: La somme de a et b.
    """

    return a + b


def subtraction(a, b):
    """
    Calcule la différence entre deux nombres.


    Paramètres:
    a (int or float): Le nombre à partir duquel on soustrait.
    b (int or float): Le nombre à soustraire.


    Retourne:
    int or float: Le résultat de a moins b.
    """

    return a - b